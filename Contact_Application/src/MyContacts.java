      private void showData(int index)
      {
        jTextFieldFname.setText(jTable2.getValueAt(index, 1).toString());
        jTextFieldLname.setText(jTable2.getValueAt(index, 2).toString());
        
        //group phone email address pic
        
        jComboBoxGroup.setSelectedItem(jTable2.getValueAt(index, 3));
        jTextFieldPhone.setText(jTable2.getValueAt(index, 4).toString());
        jTextFieldEmail.setText(jTable2.getValueAt(index, 5).toString());
        jTextFieldId.setText(jTable2.getValueAt(index, 0).toString());
        jTextAreaAddress.setText(jTable2.getValueAt(index, 6).toString());
        
        Image pic =((ImageIcon)jTable2.getValueAt(index, 7)).getImage().getScaledInstance(jLabelContactPic.getWidth(),jLabelContactPic.getHeight(),Image.SCALE_SMOOTH);
        ImageIcon img = new ImageIcon(pic);
        
        jLabelContactPic.setIcon(img);
          
      }
               
